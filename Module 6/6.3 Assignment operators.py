'''
The	assignment	operators	are	operators
that	are	used	when	assigning	
variables values,	the	most	commonly	
used	operator	being	(=).
'''
# Say
var1 = 10
print(var1)

# Add-Equal	Sign +=
var1 += 10
# var1 = var1 + 10
print(var1)

# Subtract-Equal -=
var1 -= 10
print(var1)

# Multiplication-Equal Sign:
var1 *= 10
print(var1)

# Division-Equal Sign: /=
var1 /= 10
print(var1)

# Modulus-equal sign %=
var1 %= 10