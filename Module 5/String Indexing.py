''' Indexing - Allow access string characters directly using a 
numeric value 
'''
## Postive Index - references elements from left to right
string1 = 'Munuhz' 
print(string1[2])
print(string1[5])

## Negative Indexing
string2 = "TECH"
print(string2[-2])
print(string2[-4])

## Out of the range index
print(string2[5])